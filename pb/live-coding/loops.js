// while-loops
// while (condition) {
    // statement;
// }

// Example: print the numbers from 0 to 9
let num = 0;

// while-loop
while (num < 10) {
    console.log(num);
    num += 1;
}

// do... while-loop
do {
    console.log(num);
    num += 1;
} while (num < 10);
console.log(num);

// for-loop
for (let i = 0; i < 10; i++) {
    console.log(i);
}
